package optiveTest;

public class SwitchCaseExample {
    public static void main(String[] args) {
        final int a = 1, b = 2, c = 3;
        int x = 2;
        switch (x) {
            case a:
                System.out.println("Print : " + a);
                break;
            case b:
                System.out.println("Print : " + b);
                break;
            case c:
                System.out.println("Print : " + c);
                break;
        }
    }
}
